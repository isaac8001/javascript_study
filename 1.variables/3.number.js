/**!SECTION
 * 데이터 타입 Data Type
 * 단일 데이터, 복합데이터로 나뉜다.
 */

let integer = 123;
let negative = -123;
let double = 1.23;

console.log(integer);
console.log(negative);
console.log(double);

let binary = 0b1111011; // 2진수
let octal = 0o173; // 8진수
let hex = 0x7b; // 10진수

console.log(binary);
console.log(octal);
console.log(hex);

console.log(0 / 123);
console.log(123 / 0);
console.log(123 / -0);
console.log(123 / "text");
