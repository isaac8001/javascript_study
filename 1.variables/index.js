// 변수 Variable : 값을 저장하는 공간, 자료를 저장할수 있는 이름이 주어진 기억장소
// let, const
// ex) let a = 0;
// App = 입력 -> 처리(code, 로직) -> 출력(저장, 서버통신)
// 처리 = 데이터를 임시로 저장해야함.

let a = 0;
console.log(a);
a = 1;
console.log(a);

let b;
console.log(b);

b = 2;
console.log(b);

// 변수 이름짓기 : 저장된 값을 잘 나타낼 수 있는 의미있는 이름 "구체적일 수록 좋다"
